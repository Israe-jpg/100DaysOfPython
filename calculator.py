def calculate(a, b, operation):
    if operation == "+":
        return a + b
    elif operation == "-":
        return a - b
    elif operation == "*":
        return a * b
    elif operation == "/":
        return a / b

calculating =  True
while calculating:
    a = int(input("What's the first number ? "))
    result = a
    continue_with_result = True
    while continue_with_result:
        operation = input("pick an operation : ")
        b = int(input("What's the next number ? "))
        result = int(calculate(a, b, operation))
        print(f"{a} {operation} {b} = {result}")
        go_on = input(f"type yes to continue calculating with {result}, type no to start new calculation: ") 
        if go_on.lower() == "no":
            continue_with_result = False
